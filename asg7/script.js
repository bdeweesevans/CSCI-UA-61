$(document).ready(function(){
    $("#madlibButton").click(function(){
        $("#special").css("color", "green");
        $(".highlight").css("background-color", "yellow");

        $("p").each(function(){
            $(this).css({
                "color": "blue",
                "background-color": "lightgrey",
                "width": "80%",
                "border": "1px solid black"
            });

            alert($(this).html());
        });
    });
});

